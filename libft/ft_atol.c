#include "libft.h"

long	ft_atol(char *str)
{
	long	nb;
	int		i;
	int		j;

	i = 0;
	j = 0;
	nb = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			j++;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
		nb = nb * 10 + (str[i++] - '0');
	if (j != 0)
		nb = -nb;
	return (nb);
}
